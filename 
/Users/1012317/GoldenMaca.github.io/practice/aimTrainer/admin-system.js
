// Give a skin to a player
async function adminGiveSkin(uid, skinId) {
    if (!isAdminLoggedIn()) {
        return { success: false, error: 'Admin not authenticated' };
    }
    
    if (!window.firebaseReady) {
        return { success: false, error: 'Firebase not ready' };
    }
    
    // Validate skin ID
    const validSkins = ['default', 'neonBlue', 'fireRed', 'golden', 'rainbow', 'toxic', 'purple', 'white', 'black', 'orange'];
    if (!validSkins.includes(skinId)) {
        return { success: false, error: 'Invalid skin ID' };
    }
    
    try {
        const { getDoc, updateDoc, doc } = await import("https://www.gstatic.com/firebasejs/9.22.0/firebase-firestore.js");
        
        const docRef = doc(window.db, 'users', uid);
        const docSnap = await getDoc(docRef);
        
        if (!docSnap.exists()) {
            return { success: false, error: 'Player not found' };
        }
        
        const currentSkins = docSnap.data().skins || ['default'];
        
        if (currentSkins.includes(skinId)) {
            return { success: false, error: 'Player already owns this skin' };
        }
        
        const newSkins = [...currentSkins, skinId];
        
        await updateDoc(docRef, {
            skins: newSkins,
            lastSkinGrant: Date.now(),
            skinGrantId: skinId,
            skinGrantBy: 'Admin'
        });
        
        console.log(`🎨 Admin: Gave skin ${skinId} to player ${uid}`);
        return { success: true, message: `Gave ${skinId} skin to player` };
    } catch (e) {
        console.error('Error giving skin:', e);
        return { success: false, error: e.message };
    }
}

