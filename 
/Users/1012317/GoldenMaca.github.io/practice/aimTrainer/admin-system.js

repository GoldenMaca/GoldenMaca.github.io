// Admin session state
let adminSession = {
    isAuthenticated: false,
    loginTime: null,
    username: null
};

// Check if admin is logged in
function isAdminLoggedIn() {
    if (!adminSession.isAuthenticated) return false;
    
    // Check if session has expired
    const now = Date.now();
    const sessionAge = now - adminSession.loginTime;
    
    if (sessionAge > ADMIN_CONFIG.sessionDuration) {
        // Session expired, log out
        adminLogout();
        return false;
    }
    
    return true;
}

// Admin login - verifies against Firebase
async function adminLogin(inputUsername, password) {
    // Verify username first
    if (inputUsername !== ADMIN_CONFIG.username) {
        console.log('Admin login failed: invalid username');
        return { success: false, error: 'Invalid credentials' };
    }
    
    // Verify password against Firebase
    const isValid = await verifyAdminPassword(password);
    
    if (!isValid) {
        console.log('Admin login failed: invalid password');
        return { success: false, error: 'Invalid credentials' };
    }
    
    // Create admin session
    adminSession = {
        isAuthenticated: true,
        loginTime: Date.now(),
        username: ADMIN_CONFIG.username
    };
    
    // Store session in sessionStorage (cleared on tab close)
    sessionStorage.setItem('aimtrainer_admin_session', JSON.stringify(adminSession));
    
    console.log('✅ Admin logged in successfully');
    return { success: true };
}

// Verify admin password against Firebase
async function verifyAdminPassword(password) {
    if (!window.firebaseReady) {
        console.log('Firebase not ready for admin verification');
        return false;
    }
    
    try {
        const { getDoc, doc } = await import("https://www.gstatic.com/firebasejs/9.22.0/firebase-firestore.js");
        
        // Get the admin config from Firebase
        const docRef = doc(window.db, 'admin_config', 'credentials');
        const docSnap = await getDoc(docRef);
        
        if (!docSnap.exists()) {
            console.log('Admin credentials not found in Firebase');
            return false;
        }
        
        const data = docSnap.data();
        const storedHash = data.passwordHash;
        const salt = data.salt || 'default_salt';
        
        // Hash the input password
        const hashedInput = await simpleHash(password + salt);
        
        return hashedInput === storedHash;
    } catch (e) {
        console.error('Error verifying admin password:', e);
        return false;
    }
}

// Simple hash function for password verification
async function simpleHash(str) {
    const encoder = new TextEncoder();
    const data = encoder.encode(str);
    const hashBuffer = await crypto.subtle.digest('SHA-256', data);
    const hashArray = Array.from(new Uint8Array(hashBuffer));
    return hashArray.map(b => b.toString(16).padStart(2, '0')).join('');
}

// Admin logout
function adminLogout() {
    adminSession = {
        isAuthenticated: false,
        loginTime: null,
        username: null
    };
    sessionStorage.removeItem('aimtrainer_admin_session');
    console.log('Admin logged out');
}

// Restore admin session from sessionStorage
function restoreAdminSession() {
    try {
        const stored = sessionStorage.getItem('aimtrainer_admin_session');
        if (stored) {
            adminSession = JSON.parse(stored);
            
            // Verify session is still valid
            if (isAdminLoggedIn()) {
                console.log('✅ Admin session restored');
                return true;
            }
        }
    } catch (e) {
        console.error('Error restoring admin session:', e);
    }
    return false;
}

