// ==================== SENSITIVITY CONVERTER ====================
const GAME_SENS = {
    valorant: { name: 'Valorant', eDPI: true, scaling: 1 },
    csgo: { name: 'CS2', eDPI: false, scaling: 1 },
    apex: { name: 'Apex Legends', eDPI: true, scaling: 1 },
    overwatch: { name: 'Overwatch 2', eDPI: true, scaling: 1 },
    siege: { name: 'Rainbow Six', eDPI: true, scaling: 1 },
    fortnite: { name: 'Fortnite', eDPI: true, scaling: 1 },
    pubg: { name: 'PUBG', eDPI: true, scaling: 1 },
    cod: { name: 'Call of Duty', eDPI: false, scaling: 1 },
    warzone: { name: 'Warzone', eDPI: false, scaling: 1 },
    league: { name: 'League of Legends', eDPI: false, scaling: 1 },
    osu: { name: 'osu!', eDPI: false, scaling: 1 }
};

function convertSensitivity(value, fromGame, toGame, dpi = 800, scoped = false) {
    // Normalize to a common reference (cm/360)
    let cmPer360;
    
    const from = GAME_SENS[fromGame];
    const to = GAME_SENS[toGame];
    
    if (!from || !to) return null;
    
    // Calculate cm/360
    if (from.eDPI) {
        // Using eDPI (sensitivity × DPI)
        cmPer360 = 3.493 * 10000 / (value * dpi);
    } else {
        // Using raw sensitivity
        cmPer360 = 3.493 * 10000 / value;
    }
    
    // Convert to target game
    let result;
    if (to.eDPI) {
        result = cmPer360 * dpi / 10000 * 3.493;
    } else {
        result = cmPer360 * 10000 / 3.493;
    }
    
    return Math.round(result * 100) / 100;
}

function formatSensitivity(value, game) {
    const g = GAME_SENS[game];
    if (!g) return value;
    return g.eDPI ? `${value} eDPI` : value.toFixed(2);
}

