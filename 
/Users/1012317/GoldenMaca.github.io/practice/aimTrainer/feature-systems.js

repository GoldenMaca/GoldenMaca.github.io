// ==================== 1V1 MATCHMAKING ====================
let matchmakingQueue = {};
let matchInProgress = null;

async function joinQueue(gameMode) {
    if (!window.currentUser) {
        return { success: false, error: 'Must be logged in' };
    }
    
    const playerId = window.currentUser.uid;
    const playerName = window.userProfile?.username || 'Player';
    
    if (!matchmakingQueue[gameMode]) {
        matchmakingQueue[gameMode] = [];
    }
    
    // Check if there's someone waiting
    const waitingPlayer = matchmakingQueue[gameMode].find(p => p.id !== playerId);
    
    if (waitingPlayer) {
        // Found a match!
        const matchId = generateMatchId();
        
        matchInProgress = {
            id: matchId,
            gameMode: gameMode,
            player1: waitingPlayer,
            player2: { id: playerId, name: playerName },
            status: 'playing',
            scores: { [waitingPlayer.id]: 0, [playerId]: 0 },
            startTime: Date.now()
        };
        
        // Remove from queue
        matchmakingQueue[gameMode] = matchmakingQueue[gameMode].filter(p => p.id !== playerId);
        
        localStorage.setItem('aimtrainer_match', JSON.stringify(matchInProgress));
        
        return { success: true, match: matchInProgress, opponent: waitingPlayer };
    } else {
        // Add to queue
        matchmakingQueue[gameMode].push({ id: playerId, name: playerName, joinedAt: Date.now() });
        
        return { success: true, queued: true, position: matchmakingQueue[gameMode].length };
    }
}

function leaveQueue(gameMode) {
    if (!window.currentUser) return;
    
    const playerId = window.currentUser.uid;
    
    if (matchmakingQueue[gameMode]) {
        matchmakingQueue[gameMode] = matchmakingQueue[gameMode].filter(p => p.id !== playerId);
    }
}

function getQueueCount(gameMode) {
    if (!matchmakingQueue[gameMode]) return 0;
    return matchmakingQueue[gameMode].length;
}

function getTotalQueueCount() {
    let total = 0;
    for (const mode in matchmakingQueue) {
        total += matchmakingQueue[mode].length;
    }
    return total;
}

async function submitMatchScore(score) {
    if (!matchInProgress || !window.currentUser) return;
    
    const playerId = window.currentUser.uid;
    matchInProgress.scores[playerId] = score;
    
    localStorage.setItem('aimtrainer_match', JSON.stringify(matchInProgress));
}

async function endMatch(winnerId) {
    if (!matchInProgress) return;
    
    const winner = matchInProgress.player1.id === winnerId ? matchInProgress.player1 : matchInProgress.player2;
    const loser = matchInProgress.player1.id === winnerId ? matchInProgress.player2 : matchInProgress.player1;
    
    // Award coins to winner
    const coinReward = 50; // Base reward
    await window.addCoins(coinReward);
    
    // Update stats
    const winnerWins = parseInt(localStorage.getItem('aimtrainer_1v1_wins') || '0');
    localStorage.setItem('aimtrainer_1v1_wins', winnerWins + 1);
    
    matchInProgress = null;
    localStorage.removeItem('aimtrainer_match');
    
    return { winner: winner, loser: loser, reward: coinReward };
}

function generateMatchId() {
    return 'match_' + Date.now() + '_' + Math.random().toString(36).substr(2, 9);
}

// ==================== WEEKLY CLAN POT ====================
async function addToClanPot(amount) {
    if (!currentClan) return;
    
    currentClan.coins = (currentClan.coins || 0) + amount;
    localStorage.setItem('aimtrainer_clan', JSON.stringify(currentClan));
    
    if (window.firebaseReady && window.db) {
        try {
            const { updateDoc, doc } = await import("https://www.gstatic.com/firebasejs/9.22.0/firebase-firestore.js");
            await updateDoc(doc(window.db, 'clans', currentClan.id), {
                coins: currentClan.coins
            });
        } catch (e) {
            console.log('Clan pot update error:', e);
        }
    }
}

function calculatePotSplit() {
    if (!currentClan || !currentClan.members) return null;
    
    const totalCoins = currentClan.coins || 0;
    const memberCount = currentClan.members.length;
    const potAmount = Math.floor(totalCoins * CLAN_POT_PERCENTAGE);
    const perMember = Math.floor(potAmount / memberCount);
    const remaining = potAmount % memberCount;
    
    return {
        total: totalCoins,
        potAmount: potAmount,
        perMember: perMember,
        remaining: remaining,
        memberCount: memberCount
    };
}

