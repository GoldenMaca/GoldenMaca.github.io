async function createClan(clanName, tag) {
    if (!window.firebaseReady || !window.currentUser) {
        return { success: false, error: 'Must be logged in' };
    }
    
    try {
        const { addDoc, collection, serverTimestamp } = await import("https://www.gstatic.com/firebasejs/9.22.0/firebase-firestore.js");
        
        const clanData = {
            name: clanName,
            tag: tag.toUpperCase(),
            leaderId: window.currentUser.uid,
            leaderName: window.userProfile?.username || 'Leader',
            members: [window.currentUser.uid],
            memberNames: [window.userProfile?.username || 'Leader'],
            coins: 0,
            weeklyWins: 0,
            totalWins: 0,
            createdAt: Date.now(),
            inviteCode: generateClanCode()
        };
        
        const docRef = await addDoc(collection(window.db, 'clans'), clanData);
        
        currentClan = { id: docRef.id, ...clanData };
        localStorage.setItem('aimtrainer_clan', JSON.stringify(currentClan));
        
        return { success: true, clan: currentClan };
    } catch (e) {
        return { success: false, error: e.message };
    }
}

async function joinClan(inviteCode) {
    if (!window.firebaseReady || !window.currentUser) {
        return { success: false, error: 'Must be logged in' };
    }
    
    try {
        const { getDocs, query, where, updateDoc, doc, getDoc } = await import("https://www.gstatic.com/firebasejs/9.22.0/firebase-firestore.js");
        
        const q = query(collection(window.db, 'clans'), where('inviteCode', '==', inviteCode.toUpperCase()));
        const snapshot = await getDocs(q);
        
        if (snapshot.empty) {
            return { success: false, error: 'Invalid invite code' };
        }
        
        const clanDoc = snapshot.docs[0];
        const clanData = clanDoc.data();
        
        if (clanData.members.includes(window.currentUser.uid)) {
            return { success: false, error: 'Already in this clan' };
        }
        
        await updateDoc(doc(window.db, 'clans', clanDoc.id), {
            members: [...clanData.members, window.currentUser.uid],
            memberNames: [...clanData.memberNames, window.userProfile?.username || 'Member']
        });
        
        currentClan = { id: clanDoc.id, ...clanData };
        currentClan.members.push(window.currentUser.uid);
        localStorage.setItem('aimtrainer_clan', JSON.stringify(currentClan));
        
        return { success: true, clan: currentClan };
    } catch (e) {
        return { success: false, error: e.message };
    }
}

function leaveClan() {
    currentClan = null;
    localStorage.removeItem('aimtrainer_clan');
}

